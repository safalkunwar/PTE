async function seed() {
  const conn = await mysql.createConnection(process.env.DATABASE_URL);
  
  // Check if already seeded
  const [rows] = await conn.execute("SELECT COUNT(*) as count FROM questions");
  const count = rows[0].count;
  
  if (count > 0) {
    console.log(`Questions already seeded (${count} questions). Skipping.`);
    await conn.end();
    return;
  }

  console.log(`Seeding ${questions.length} questions...`);
  
  for (const q of questions) {
    await conn.execute(
      `INSERT INTO questions (section, taskType, difficulty, title, prompt, content, audioUrl, imageUrl, options, correctAnswer, modelAnswer, wordLimit, timeLimit, preparationTime, tags)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
      [
        q.section, q.taskType, q.difficulty, q.title, q.prompt || null,
        q.content || null, q.audioUrl || null, q.imageUrl || null,
        q.options || null, q.correctAnswer || null, q.modelAnswer || null,
        q.wordLimit || null, q.timeLimit || null, q.preparationTime || null,
        q.tags || null,
      ]
    );
  }
  
  console.log("✅ Questions seeded successfully!");
  await conn.end();
}

seed().catch(console.error);
